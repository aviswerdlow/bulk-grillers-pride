#!/bin/bash
# Execute cutover and archive board
# Task: T169 - Execute cutover and archive board
# This script performs the final cutover from board to GitHub task management

set -e

# Configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BOARD_FILE="${BOARD_FILE:-AGENTS_BOARD.md}"
ARCHIVE_DIR="${ARCHIVE_DIR:-.board_archive}"
CUTOVER_LOG="cutover_$(date +%Y%m%d_%H%M%S).log"
CUTOVER_REPORT="cutover_report_$(date +%Y%m%d_%H%M%S).md"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
MAGENTA='\033[0;35m'
NC='\033[0m'

# Log function
log() {
    echo -e "$1" | tee -a "$CUTOVER_LOG"
}

log_header() {
    echo -e "\n${BLUE}=== $1 ===${NC}" | tee -a "$CUTOVER_LOG"
}

log_success() {
    echo -e "${GREEN}✓ $1${NC}" | tee -a "$CUTOVER_LOG"
}

log_warning() {
    echo -e "${YELLOW}⚠ $1${NC}" | tee -a "$CUTOVER_LOG"
}

log_error() {
    echo -e "${RED}✗ $1${NC}" | tee -a "$CUTOVER_LOG"
}

# Pre-cutover validation
validate_readiness() {
    log_header "Pre-Cutover Validation"
    
    # Check T167 completion
    if [ -f "T167_verification_report.md" ] || ls final_verification_report_*.md >/dev/null 2>&1; then
        log_success "T167 verification completed"
    else
        log_error "T167 verification not found - run final sync first"
        return 1
    fi
    
    # Check rollback procedure
    if [ -f "$SCRIPT_DIR/rollback.sh" ]; then
        log_success "Rollback procedure available"
    else
        log_error "Rollback script not found"
        return 1
    fi
    
    # Check current mode
    local current_mode="${TASK_SYSTEM:-board}"
    log "Current TASK_SYSTEM: $current_mode"
    
    # Confirm with user
    echo ""
    echo -e "${YELLOW}⚠️  CUTOVER WARNING ⚠️${NC}"
    echo "This will:"
    echo "  1. Set GitHub as the default task system"
    echo "  2. Archive the current AGENTS_BOARD.md"
    echo "  3. Update all agent environments"
    echo "  4. Make GitHub Issues the source of truth"
    echo ""
    echo -e "${CYAN}Are you ready to proceed with cutover? (yes/no):${NC}"
    read -r response
    
    if [ "$response" != "yes" ]; then
        log "Cutover cancelled by user"
        return 1
    fi
    
    return 0
}

# Create final board archive
archive_board() {
    log_header "Creating Board Archive"
    
    # Create archive directory
    mkdir -p "$ARCHIVE_DIR"
    local timestamp=$(date +%Y%m%d_%H%M%S)
    local archive_name="AGENTS_BOARD_FINAL_${timestamp}.md"
    
    # Copy board with metadata
    {
        echo "# ARCHIVED - $(date)"
        echo "# Final state before GitHub cutover"
        echo "# Total tasks: $(grep -c "^| T[0-9]" "$BOARD_FILE" || echo 0)"
        echo ""
        cat "$BOARD_FILE"
    } > "$ARCHIVE_DIR/$archive_name"
    
    log_success "Board archived to: $ARCHIVE_DIR/$archive_name"
    
    # Create summary
    local total_tasks=$(grep -c "^| T[0-9]" "$BOARD_FILE" || echo 0)
    local done_tasks=$(grep -c "✔️ done" "$BOARD_FILE" || echo 0)
    local pending_tasks=$((total_tasks - done_tasks))
    
    log "Archive summary:"
    log "  - Total tasks: $total_tasks"
    log "  - Completed: $done_tasks"
    log "  - Pending: $pending_tasks"
    
    # Compress archive
    if command -v gzip &> /dev/null; then
        gzip -k "$ARCHIVE_DIR/$archive_name"
        log_success "Compressed archive created"
    fi
    
    return 0
}

# Update system configuration
update_system_config() {
    log_header "Updating System Configuration"
    
    # Update .env file
    if [ -f .env ]; then
        # Remove old TASK_SYSTEM entries
        sed -i.bak '/TASK_SYSTEM=/d' .env
        log "Cleaned .env file"
    fi
    
    # Set GitHub as default
    echo "TASK_SYSTEM=github" >> .env
    log_success "Set TASK_SYSTEM=github in .env"
    
    # Export for current session
    export TASK_SYSTEM=github
    
    # Create system config file
    cat > .task_system_config << EOF
# Task System Configuration
# Generated by cutover script on $(date)

# Default task system
TASK_SYSTEM=github

# Board archive location
BOARD_ARCHIVE=$ARCHIVE_DIR

# Cutover timestamp
CUTOVER_DATE=$(date -u +%Y-%m-%dT%H:%M:%SZ)

# Migration version
MIGRATION_VERSION=1.0

# Emergency rollback command
ROLLBACK_COMMAND="./scripts/migration/rollback.sh"
EOF
    
    log_success "Created .task_system_config"
    
    return 0
}

# Update board file with cutover notice
update_board_notice() {
    log_header "Updating Board with Cutover Notice"
    
    # Create new board content
    cat > "$BOARD_FILE" << 'EOF'
# AGENTS_BOARD.md - ARCHIVED

## 🚨 IMPORTANT NOTICE 🚨

This board has been **ARCHIVED** and is no longer the active task management system.

### Current Status: MIGRATED TO GITHUB ISSUES

As of **CUTOVER_DATE**, all task management has been migrated to GitHub Issues.

## For All Agents

### How to Access Tasks

```bash
# Set your agent name
export AGENT_NAME="your-agent-name"

# Load task functions
source scripts/migration/task_lib.sh

# View your tasks
get_my_tasks "$AGENT_NAME"
```

### GitHub Web Interface

Visit the repository's Issues tab to:
- View all tasks
- Filter by agent labels
- Track task progress
- Add comments and updates

### Quick Reference

- **View Tasks**: `get_my_tasks "agent-name"`
- **Claim Task**: `claim_task "T123" "agent-name"`
- **Update Status**: `update_task_status "T123" "in-progress"`
- **Add Comment**: `add_task_comment "T123" "Progress update"`

## Emergency Rollback

If you need to temporarily revert to board mode:

```bash
# Quick rollback
export TASK_SYSTEM=board

# Full rollback
./scripts/migration/rollback.sh
```

## Archive Location

The final board state is preserved at:
`ARCHIVE_PATH`

## Support

- **Issues**: Create with label 'migration-issue'
- **Logs**: Check `.sync.log` for sync details
- **Help**: See migration documentation

---

**Cutover completed on CUTOVER_DATE**
**Migration executed by: migration-agent**
EOF
    
    # Replace placeholders
    sed -i "s|CUTOVER_DATE|$(date)|g" "$BOARD_FILE"
    sed -i "s|ARCHIVE_PATH|$ARCHIVE_DIR|g" "$BOARD_FILE"
    
    log_success "Updated board with cutover notice"
    
    return 0
}

